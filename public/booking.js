        function handleFlightSearch() {
            const origin = document.getElementById("origin").value;
            const destination = document.getElementById("destination").value;
            const departureDate = document.getElementById("departure-date").value;
            const returnDate = document.getElementById("return-date").value;
            const cabinClass = document.getElementById("cabin-class").value;
            const adults = parseInt(document.getElementById("adults").value);
            const children = parseInt(document.getElementById("children").value);
            const infants = parseInt(document.getElementById("infants").value);
            
         if (!origin || !destination || !departureDate) {
                alert("Please fill in all required fields");
                return;
            }
            
            if (state.tripType === "round-trip" && !returnDate) {
                alert("Please select a return date");
                return;
            }
            
            if (origin === destination) {
                alert("Origin and destination cannot be the same");
                return;
            }
            
           const departureDateObj = new Date(departureDate);
            const returnDateObj = new Date(returnDate);
            
            const options = { weekday: 'short', year: 'numeric', month: 'short', day: 'numeric' };
            const formattedDepartureDate = departureDateObj.toLocaleDateString('en-US', options);
            const formattedReturnDate = returnDateObj.toLocaleDateString('en-US', options);
            
            outboundDateDisplay.textContent = `${getCityName(origin)} (${origin}) to ${getCityName(destination)} (${destination}) - ${formattedDepartureDate}`;
            
            if (state.tripType === "round-trip") {
                returnDateDisplay.textContent = `${getCityName(destination)} (${destination}) to ${getCityName(origin)} (${origin}) - ${formattedReturnDate}`;
                returnFlightSection.classList.remove("hidden");
            } else {
                returnFlightSection.classList.add("hidden");
            }
            
           state.passengers = [];
            for (let i = 0; i < adults; i++) {
                state.passengers.push({ type: "adult" });
            }
            for (let i = 0; i < children; i++) {
                state.passengers.push({ type: "child" });
            }
            for (let i = 0; i < infants; i++) {
                state.passengers.push({ type: "infant" });
            }
            
            generatePassengerForms();
            
            const route = `${origin}-${destination}`;
            const outboundFlights = flightsData[route] || [];
            
           displayFlights(outboundFlights, outboundFlightsContainer, "outbound");
            
            if (state.tripType === "round-trip") {
                const returnRoute = `${destination}-${origin}`;
                const returnFlights = flightsData[returnRoute] || [];
                displayFlights(returnFlights, returnFlightsContainer, "return");
            } else {
                returnFlightsContainer.innerHTML = "";
            }
            
            showSection("flight-results");
        }

