        function handlePassengerDetails() {
            let isValid = true;
            
            state.passengers.forEach((passenger, index) => {
                const title = document.getElementById(`passenger-${index}-title`).value;
                const firstName = document.getElementById(`passenger-${index}-first-name`).value;
                const lastName = document.getElementById(`passenger-${index}-last-name`).value;
                const nationality = document.getElementById(`passenger-${index}-nationality`).value;
                
                if (!title || !firstName || !lastName || !nationality) {
                    isValid = false;
                    return;
                }
                
                if (passenger.type !== "infant") {
                    const passport = document.getElementById(`passenger-${index}-passport`).value;
                    const expiry = document.getElementById(`passenger-${index}-expiry`).value;
                    
                    if (!passport || !expiry) {
                        isValid = false;
                        return;
                    }
                }
                
                if (passenger.type === "child" || passenger.type === "infant") {
                    const dob = document.getElementById(`passenger-${index}-dob`).value;
                    
                    if (!dob) {
                        isValid = false;
                        return;
                    }
                }
                
                if (passenger.type === "infant") {
                    const guardian = document.getElementById(`passenger-${index}-guardian`).value;
                    
                    if (!guardian) {
                        isValid = false;
                        return;
                    }
                }
                
             passenger.title = title;
                passenger.firstName = firstName;
                passenger.lastName = lastName;
                passenger.nationality = nationality;
                
                if (passenger.type !== "infant") {
                    passenger.passportNumber = document.getElementById(`passenger-${index}-passport`).value;
                    passenger.passportExpiry = document.getElementById(`passenger-${index}-expiry`).value;
                }
                
                if (passenger.type === "child" || passenger.type === "infant") {
                    passenger.dob = document.getElementById(`passenger-${index}-dob`).value;
                }
                
                if (passenger.type === "infant") {
                    passenger.guardian = document.getElementById(`passenger-${index}-guardian`).value;
                }
            });
            
            if (!isValid) {
                alert("Please fill in all required passenger details");
                return;
            }
            
            updateBookingSummary();
            
            showSection("payment-section");
        }

