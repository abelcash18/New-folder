        function handlePayment() {
            const cardNumber = document.getElementById("card-number").value;
            const cardName = document.getElementById("card-name").value;
            const expiryMonth = document.getElementById("expiry-month").value;
            const expiryYear = document.getElementById("expiry-year").value;
            const cvv = document.getElementById("cvv").value;
            const billingFirstName = document.getElementById("billing-first-name").value;
            const billingLastName = document.getElementById("billing-last-name").value;
            const billingAddress = document.getElementById("billing-address").value;
            const billingCity = document.getElementById("billing-city").value;
            const billingCountry = document.getElementById("billing-country").value;
            
            if (!cardNumber || !cardName || !expiryMonth || !expiryYear || !cvv || 
                !billingFirstName || !billingLastName || !billingAddress || !billingCity || !billingCountry) {
                alert("Please fill in all required payment details");
                return;
            }
            
            if (!validateCardNumber(cardNumber)) {
                alert("Please enter a valid credit card number");
                return;
            }
            
            const currentDate = new Date();
            const currentYear = currentDate.getFullYear();
            const currentMonth = currentDate.getMonth() + 1;
            
            if (parseInt(expiryYear) < currentYear || 
                (parseInt(expiryYear) === currentYear && parseInt(expiryMonth) < currentMonth)) {
                alert("Card has expired");
                return;
            }
            
            if (cvv.length < 3 || cvv.length > 4 || !/^\d+$/.test(cvv)) {
                alert("Please enter a valid CVV");
                return;
            }
            
             state.paymentDetails = {
                cardNumber: cardNumber,
                cardName: cardName,
                expiryMonth: expiryMonth,
                expiryYear: expiryYear,
                cvv: cvv,
                billingAddress: {
                    firstName: billingFirstName,
                    lastName: billingLastName,
                    address: billingAddress,
                    city: billingCity,
                    country: billingCountry,
                    state: document.getElementById("billing-state").value,
                    zip: document.getElementById("billing-zip").value
                }
            };
            
           updateConfirmationDetails();
            
           showSection("confirmation-section");
        }

        function validateCardNumber(cardNumber) {
         const cleaned = cardNumber.replace(/\s+/g, '');
            return /^\d{13,19}$/.test(cleaned);
        }

